public class IncrementDecrement01 {
    public static void main(String[] args) {

        int a = 5;

        System.out.println(a); //5

        // Increment

        a = a + 2; // veya a +=  2;

        System.out.println(a); //7

        int b = 10;  // 10

        b += 5;  // 15

        b = b + 5;  //20

        // Decrement

        int c = 8 ; // 8

        c = c - 3;  //5

        c-= 3 ; // 2

        // Increment 2 :

        int d = 6;

        d= d * 2; // 12

        d *= 2 ;  // 24






    }
}
